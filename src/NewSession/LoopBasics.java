package NewSession;

import java.util.Scanner;

public class LoopBasics {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("KRITI");
        int n = sc.nextInt();
        for(int i = 0; i<=n; i++){
            System.out.println("KRITI KUMARI");
        }
    }
}
